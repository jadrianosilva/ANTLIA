CREATE OR ALTER PROCEDURE SP_CONSULTAR_MOVIMENTOS
AS
BEGIN

```
SELECT
    MM.DAT_MES,
    MM.DAT_ANO,
    MM.COD_PRODUTO,
    P.DES_PRODUTO,
    MM.NUM_LANCAMENTO,
    MM.DES_DESCRICAO,
    MM.VAL_VALOR,
    MM.DAT_MOVIMENTO
FROM MOVIMENTO_MANUAL MM
INNER JOIN PRODUTO P
    ON P.COD_PRODUTO = MM.COD_PRODUTO
ORDER BY
    MM.DAT_ANO,
    MM.DAT_MES,
    MM.NUM_LANCAMENTO;
```

END;
GO
